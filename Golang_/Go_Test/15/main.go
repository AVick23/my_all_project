/*Напишите функцию, которая принимает массив целых чисел и возвращает новый массив, содержащий только уникальные элементы (без дубликатов).*/

func uniq(nums []int) []int {
	// unique - это мапа, где ключи - это уникальные элементы из nums,
	// а значения - просто true. Это позволяет быстро проверять,
	// является ли элемент уникальным.
	unique := make(map[int]bool)

	// result - это срез, который будет содержать уникальные элементы.
	var result []int

	// Мы проходим по всем элементам nums.
	for _, num := range nums {
		// Если элемент не уникальный, то unique[num] будет true,
		// и мы его пропускаем.
		if !unique[num] {
			// Помечаем элемент как уникальный.
			unique[num] = true
			// Добавляем его в result.
			result = append(result, num)
		}
	}

	// Возвращаем result.
	return result
}

